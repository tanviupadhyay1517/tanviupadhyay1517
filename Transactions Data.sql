CASE
	WHEN city_code BETWEEN 2 AND 5 THEN 'Metro'
    WHEN city_code BETWEEN 6 AND 10 THEN 'Urban'
    ELSE 'Rural'
END As city_category
FROM customer.custdata;

-- 80. Product Popularity by Sales
SELECT p.prod_subcat,
CASE 
	WHEN SUM(t.Qty) > 100 THEN 'High popular'
    WHEN SUM(t.Qty) BETWEEN 70 AND 100 THEN 'Moderately popular'
    ELSE 'Less popular'
END AS popularity
FROM prodcat.prodata p
JOIN transactions.transdata t
ON t.prod_subcat_code = p.prod_sub_cat_code
GROUP BY p.prod_subcat;

-- 81. Transaction Discount Status
