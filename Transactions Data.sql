SELECT transaction_id, total_amt,
CASE 
	WHEN total_amt >= 5000 THEN 'High Value'
    ELSE 'Low Value'
END AS transaction_type
FROM transactions.transdata;

-- 73. Gender Labeling
SELECT customer_id, gender,
CASE
	WHEN gender = 'M' THEN 'MALE'
    WHEN gender = 'F' THEN 'FEMALE'
    ELSE 'Other'
END AS Gender_label
FROM customer.custdata;

-- 74. Age Group Classification
SELECT customer_Id,
       TIMESTAMPDIFF(YEAR, DOB, CURDATE()) AS age,
       CASE
           WHEN TIMESTAMPDIFF(YEAR, DOB, CURDATE()) < 18 THEN 'Minor'
           WHEN TIMESTAMPDIFF(YEAR, DOB, CURDATE()) BETWEEN 18 AND 40 THEN 'Adult'
           ELSE 'Senior'
       END AS age_group
FROM customer.custdata;

-- 75. Tax Bracket Categorization
SELECT transaction_id, tax,
CASE
	WHEN tax = 0 THEN 'No tax'
    WHEN tax BETWEEN 1 AND 50 THEN 'Low tax'
    WHEN tax BETWEEN 51 AND 200 THEN 'Medium tax'
    ELSE 'High tax'
END AS tax_category
FROM transactions.transdata;

-- 76. Product Category Type
SELECT prod_cat_code, prod_cat,
CASE
	WHEN prod_cat IN ('Electronics', 'computers') THEN 'Tech'
    WHEN prod_cat IN ('Clothing', 'Shoes') THEN 'Fashion'
    ELSE 'Other'
END AS category_type
FROM prodcat.prodata;

-- 77. Weekend vs Weekday Transactions
