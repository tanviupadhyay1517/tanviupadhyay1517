-- using joins
-- 1. Show all transactions with their product category name
SELECT t.transaction_id, t.total_amt, p.prod_cat
FROM transactions.transdata t
JOIN prodcat.prodata p
ON t. prod_cat_code = p.prod_cat_code;

-- 2. List each transaction with customer gender and total amount.
SELECT t.transaction_id, c.Gender, t.total_amt
FROM transactions.transdata t
JOIN customer.custdata c
ON t.cust_id = c.customer_id;

-- 3. Show product category and subcategory for each transaction.
SELECT t.transaction_id, p.prod_cat, p.prod_subcat
FROM transactions.transdata t
JOIN prodcat.prodata p
ON t.prod_cat_code = p.prod_cat_code
AND t.prod_subcat_code = p.prod_sub_cat_code;

-- 4. Get the total quantity purchased by each customer city.
SELECT c.city_code, SUM(t.Qty) As Total_Qty
FROM customer.custdata c
JOIN transactions.transdata t
ON c.customer_Id = t.cust_id
GROUP BY c.city_code;

-- 5. Retrive the total revenue generated per product category.
SELECT p.prod_cat, SUM(t.total_amt) as Total_Revenue
FROM prodcat.prodata p
JOIN transactions.transdata t
ON t.prod_cat_code = p.prod_cat_code
GROUP BY p.prod_cat;

-- 6. Show each transaction with customer city and product subcategory name.
SELECT t.transaction_id, c.city_code, p.prod_subcat 
FROM transactions.transdata t
JOIN customer.custdata c
ON c.customer_Id = t.cust_id 
JOIN prodcat.prodata p
ON t.prod_cat_code = p.prod_cat_code AND t.prod_subcat_code = p.prod_sub_cat_code;

-- 7. Find the number of transactions done by each gender.
SELECT c.gender, COUNT(transaction_id) As Total
FROM transactions.transdata t
JOIN customer.custdata c
ON c.customer_Id = t.cust_id
GROUP BY c.gender;

-- 8. List all customers who purchased from more than one store type.
SELECT c.customer_id, COUNT(DISTINCT t.store_type) As Storetype
FROM transactions.transdata t
JOIN customer.custdata c
ON c.customer_Id = t.cust_id
GROUP BY c. customer_Id
HAVING COUNT(DISTINCT t.store_type) > 1;

-- 9.show the avg tax paid per product category.
SELECT p.prod_cat, AVG(t.tax) As AVG_tax
FROM transactions.transdata t
JOIN prodcat.prodata p
ON t.prod_cat_code = p.prod_cat_code
GROUP BY p.prod_cat;

-- 10. Retrive transactions along with customer date of bith and product category. 
SELECT t.transaction_id, c.DOB, p.prod_cat, t.total_amt
FROM transactions.transdata t
JOIN customer.custdata c
ON t.cust_id = c.customer_Id
JOIN prodcat.prodata p
ON t.prod_cat_code = p.prod_cat_code;

-- 11. Find total revenue per store type and product category combination.
SELECT t.store_type, p.prod_cat, SUM(t.total_amt) AS Total_revenue
FROM transactions.transdata t
JOIN prodcat.prodata p
ON t.prod_cat_code = p.prod_cat_code
GROUP BY t.store_type, p.prod_cat;

-- 12. Show all transactions where the customer's city is the same as city_code='10'
SELECT t.transaction_id, t.total_amt, c.city_code
FROM transactions.transdata t
JOIN customer.custdata c
ON t.cust_id = c.customer_Id
